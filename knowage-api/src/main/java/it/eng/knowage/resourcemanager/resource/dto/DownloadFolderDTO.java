package it.eng.knowage.resourcemanager.resource.dto;

/**
 * @author albnale
 *
 */
public class DownloadFolderDTO {
	private String key;

	public DownloadFolderDTO() {
		super();
		// TODO Auto-generated constructor stub
	}

	public DownloadFolderDTO(String key) {
		super();
		this.key = key;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		DownloadFolderDTO other = (DownloadFolderDTO) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}

}
