package it.eng.knowage.resourcemanager.service.impl;

import static org.junit.jupiter.api.Assertions.assertDoesNotThrow;
import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertThrows;

import javax.ws.rs.core.Response;

import org.junit.jupiter.api.Test;

import it.eng.knowage.boot.error.KnowageBusinessException;

class EngGptDataValidationTest {

	private final ResourceManagerAPIImpl resourceManagerAPI = new ResourceManagerAPIImpl();

	@Test
	void validateEngGptDataPayloadAcceptsExpectedSchema() {
		String jsonPayload = "{"
				+ "\"sql_gold\":["
				+ "{"
				+ "\"NL\":\"What is the total revenue generated by each shop?\","
				+ "\"SQL\":\"SELECT 1\","
				+ "\"tables\":[\"sales\",\"shops\",\"products\"],"
				+ "\"columns\":[\"sales.quantity\",\"shops.name\",\"products.price\"]"
				+ "},"
				+ "{"
				+ "\"NL\":\"Which product has been sold the most in terms of total quantity?\","
				+ "\"SQL\":\"SELECT 2\","
				+ "\"tables\":[\"sales\",\"products\"],"
				+ "\"columns\":[\"sales.quantity\",\"products.name\"]"
				+ "}"
				+ "]"
				+ "}";

		assertDoesNotThrow(() -> resourceManagerAPI.validateEngGptDataPayload(jsonPayload));
	}

	@Test
	void validateEngGptDataPayloadRejectsMissingSqlGoldArray() {
		KnowageBusinessException thrown = assertThrows(KnowageBusinessException.class,
				() -> resourceManagerAPI.validateEngGptDataPayload("{\"foo\":[]}"));

		assertEquals(Response.Status.BAD_REQUEST, thrown.getStatus());
		assertEquals("The request body must contain the sql_gold array", thrown.getDescription());
	}

	@Test
	void validateEngGptDataPayloadRejectsMissingMandatoryField() {
		String jsonPayload = "{"
				+ "\"sql_gold\":["
				+ "{"
				+ "\"NL\":\"What is the total revenue generated by each shop?\","
				+ "\"tables\":[\"sales\"],"
				+ "\"columns\":[\"sales.quantity\"]"
				+ "}"
				+ "]"
				+ "}";

		KnowageBusinessException thrown = assertThrows(KnowageBusinessException.class,
				() -> resourceManagerAPI.validateEngGptDataPayload(jsonPayload));

		assertEquals(Response.Status.BAD_REQUEST, thrown.getStatus());
		assertEquals("Each sql_gold entry must contain a non-empty SQL string", thrown.getDescription());
	}

	@Test
	void validateEngGptDataPayloadRejectsNonStringArrayValues() {
		String jsonPayload = "{"
				+ "\"sql_gold\":["
				+ "{"
				+ "\"NL\":\"Which product has been sold the most in terms of total quantity?\","
				+ "\"SQL\":\"SELECT 2\","
				+ "\"tables\":[\"sales\",1],"
				+ "\"columns\":[\"sales.quantity\",\"products.name\"]"
				+ "}"
				+ "]"
				+ "}";

		KnowageBusinessException thrown = assertThrows(KnowageBusinessException.class,
				() -> resourceManagerAPI.validateEngGptDataPayload(jsonPayload));

		assertEquals(Response.Status.BAD_REQUEST, thrown.getStatus());
		assertEquals("The tables array must contain only non-empty strings", thrown.getDescription());
	}
}
