package it.eng.spagobi.tenant;

/**
 * @author Davide Zerbetto (davide.zerbetto@eng.it)
 */
public class Tenant {

	private String name = null;
	
	public Tenant(String name) {
		this.setName(name);
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	@Override
	public String toString() {
		return name;
	}
	
}
